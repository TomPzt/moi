def addition(a, b):
    """Calcul la somme des deux nombres passés en paramètre

    Parameters:
        a: float, premier nombre à additionner
        b: float, deuxième nombre à additionner

    Returns:
        Résultat de la somme
    """
    return a+b

def soustraction(a, b):
    """Calcul la différence des deux nombres passés en paramètre

    Parameters:
        a: float, nombre de base
        b: float, nombre à soustraire

    Returns:
        Résultat de la somme
    """
    return a-b
